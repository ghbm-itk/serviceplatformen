<?php

namespace DigitalPost\MeMo\ContactInfo;

/**
 * Class representing ContactInfoAType
 */
class ContactInfoAType
{
    /**
     * @var string $label
     */
    private $label = null;

    /**
     * @var string $value
     */
    private $value = null;

    /**
     * Gets as label
     *
     * @return string
     */
    public function getLabel()
    {
        return $this->label;
    }

    /**
     * Sets a new label
     *
     * @param string $label
     * @return self
     */
    public function setLabel($label)
    {
        $this->label = $label;
        return $this;
    }

    /**
     * Gets as value
     *
     * @return string
     */
    public function getValue()
    {
        return $this->value;
    }

    /**
     * Sets a new value
     *
     * @param string $value
     * @return self
     */
    public function setValue($value)
    {
        $this->value = $value;
        return $this;
    }
}

